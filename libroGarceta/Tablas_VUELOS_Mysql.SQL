SET SESSION FOREIGN_KEY_CHECKS=0;

DROP TABLE IF EXISTS TRIPULACION CASCADE ;
DROP TABLE IF EXISTS PASAJE CASCADE ;
DROP TABLE IF EXISTS VUELO CASCADE ;
DROP TABLE IF EXISTS PERSONAL CASCADE ;
DROP TABLE IF EXISTS PASAJERO CASCADE ;



CREATE TABLE PASAJE
(
	PASAJERO_COD smallint NOT NULL,
	IDENTIFICADOR varchar(10) NOT NULL,
	NUMASIENTO smallint,
	CLASE varchar(10),
	PVP float,
	CONSTRAINT PASAJEIDX PRIMARY KEY (PASAJERO_COD, IDENTIFICADOR)
);


CREATE TABLE PASAJERO
(
	COD smallint NOT NULL,
	NOMBRE varchar(30),
	TLF varchar(10),
	DIRECCION varchar(40),
	PAIS varchar(15),
	CONSTRAINT PKPASAJERO PRIMARY KEY (COD)
);


CREATE TABLE PERSONAL
(
	CODIGO smallint NOT NULL,
	NOMBRE varchar(20),
	CATEGORIA varchar(15),
	CONSTRAINT PKPERSONAL PRIMARY KEY (CODIGO)
);


CREATE TABLE TRIPULACION
(
	VUELO_IDENTIFICADOR varchar(10) NOT NULL,
	PERSONAL_CODIGO smallint NOT NULL,
	PUESTO varchar(10),
	CONSTRAINT TRIPULACION_IDX PRIMARY KEY (VUELO_IDENTIFICADOR, PERSONAL_CODIGO)
);


CREATE TABLE VUELO
(
	IDENTIFICADOR varchar(10) NOT NULL,
	AEROPUERTO_ORIGEN varchar(10),
	AEROPUERTO_DESTINO varchar(10),
	TIPO_VUELO varchar(15),
	FECHA_VUELO date,
	CONSTRAINT PKVUELO PRIMARY KEY (IDENTIFICADOR)
);



/* Create Foreign Keys */




ALTER TABLE PASAJE
	ADD CONSTRAINT FKPASAJEPASAJERO FOREIGN KEY (PASAJERO_COD)
	REFERENCES PASAJERO (COD)
	ON UPDATE CASCADE
	ON DELETE RESTRICT
;


ALTER TABLE TRIPULACION
	ADD CONSTRAINT FKTRIPULACIONPERSONAL FOREIGN KEY (PERSONAL_CODIGO)
	REFERENCES PERSONAL (CODIGO)
	ON UPDATE CASCADE
	ON DELETE RESTRICT
;


ALTER TABLE PASAJE
	ADD CONSTRAINT FKPASAJEVUELO FOREIGN KEY (IDENTIFICADOR)
	REFERENCES VUELO (IDENTIFICADOR)
	ON UPDATE CASCADE
	ON DELETE RESTRICT
;


ALTER TABLE TRIPULACION
	ADD CONSTRAINT FKTRIPULACIONVUELO FOREIGN KEY (VUELO_IDENTIFICADOR)
	REFERENCES VUELO (IDENTIFICADOR)
	ON UPDATE CASCADE
	ON DELETE RESTRICT
;


	



